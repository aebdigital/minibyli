function loadProduct(productId) {
    const product = productData[productId];
    if (!product) return;
    
    const productContent = document.getElementById('product-content');
    productContent.innerHTML = `
        <div class="product-hero">
            <div class="product-image-large">
                <img src="${product.image}" alt="${product.name}">
            </div>
            <h1>${product.name}</h1>
        </div>
        
        <div class="product-info">
            <div class="info-section">
                <h3><i class="fas fa-seedling"></i> Chuť:</h3>
                <p>${product.taste}</p>
            </div>
            
            <div class="info-section">
                <h3><i class="fas fa-heart"></i> Zdravotné benefity:</h3>
                <ul>
                    ${product.benefits.map(benefit => `<li>${benefit}</li>`).join('')}
                </ul>
            </div>
            
            <div class="info-section">
                <h3><i class="fas fa-utensils"></i> Využitie:</h3>
                <p>${product.usage}</p>
            </div>
        </div>
    `;
}

function setActiveTab(productId) {
    const tabs = document.querySelectorAll('.product-nav li');
    tabs.forEach(tab => tab.classList.remove('active'));
    
    const activeTab = document.querySelector(`[data-product="${productId}"]`).parentElement;
    activeTab.classList.add('active');
}

function initializeProducts() {
    const urlParams = new URLSearchParams(window.location.search);
    const productId = urlParams.get('product') || 'minikoriander';
    
    loadProduct(productId);
    setActiveTab(productId);
    
    const productLinks = document.querySelectorAll('[data-product]');
    productLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const productId = link.getAttribute('data-product');
            loadProduct(productId);
            setActiveTab(productId);
            
            const newUrl = `${window.location.pathname}?product=${productId}`;
            window.history.pushState({}, '', newUrl);
        });
    });
}

document.addEventListener('DOMContentLoaded', initializeProducts);